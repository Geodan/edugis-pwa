function supportsUsage(scheme, usage){
  if (!usage) {
    return true;
  }
  usage = Object.assign({}, {'blind': 'bad', 'print': 'bad', 'screen': 'bad', 'copy': 'bad'}, usage);
  if (usage.blind === 'maybe' && scheme.blind === 'bad') {
    return false;
  }
  if (usage.blind === 'ok' && scheme.blind !== 'ok') {
    return false;
  }
  if (usage.print === 'maybe' && scheme.print === 'bad') {
    return false;
  }
  if (usage.print === 'ok' && scheme.print !== 'ok') {
    return false;
  }
  if (usage.screen === 'maybe' && scheme.screen === 'bad') {
    return false;
  }
  if (usage.screen === 'ok' && scheme.screen !== 'ok') {
    return false;
  }
  if (usage.copy === 'maybe' && scheme.copy === 'bad') {
    return false;
  }
  if (usage.copy === 'ok' && scheme.copy !== 'ok') {
    return false;
  }
  return true;
}

/**
* @description
* gets an array of colorbrewer color schemes for the given number of classes and color scheme type
* @param {number} numClasses
* number of classes to get colors for
* @param {string} schemeType
* the color scheme type. Qualitative: 'qual' or divergent: 'div' or sequential: 'seq'
* @param {boolean} reversed
* true if colors in the scheme should be reversed in order
* @param {Object<{blind:'bad', print:'bad', screen:'bad',copy:'bad'}>} usage
* requested usage properties, 'bad': not important, 'maybe'
* @returns {Array.<{colors:[]}>}
* Array of color schemes [{colors:[],"blind":"ok","print": "bad","screen": "maybe","copy": "bad"}]
*/
export function getColorSchemes(numClasses, schemeType, reversed, usage) {
  let result = [];
  numClasses = parseInt(numClasses);
  if (isNaN(numClasses)) {
    return result;
  }
  if (numClasses <= 0) {
    return result;
  }
  if (numClasses <= 2) {
      result = colorbrewer.filter(scheme=>scheme.type===schemeType && supportsUsage(scheme.sets[0], usage))
          .map(scheme=>{
              let result = Object.assign({},scheme.sets[0]);
              let colors = result.colors.map(c=>c);
              result.colors = [colors[reversed?0:2]];
              if (numClasses === 2) {
                  result.colors.unshift(colors[reversed?2:0]);
              }
              result.name = scheme.name;
              result.type = scheme.type;
              return result;
          });
      if (result.length) {
        return result;
      }
  }
  // find set that has numClasses colors or return largest set smaller than numClasses
  for (; numClasses > 2 && result.length === 0; numClasses--) {
      result = colorbrewer.filter(scheme=>scheme.type===schemeType && scheme.sets.length > numClasses - 3 && supportsUsage(scheme.sets[numClasses - 3], usage))
          .map(scheme=>{
              let result = Object.assign({}, scheme.sets[numClasses - 3]);
              result.name = scheme.name;
              result.type = scheme.type;
              return result;
          });
  }
  if (result.length) {
      if (reversed) {
          result.forEach(scheme=>scheme.colors = scheme.colors.map(c=>c).reverse())
      }
  } else {
    result = [{colors:['#ff0000']}];
  }
  return result;
}

export default getColorSchemes;